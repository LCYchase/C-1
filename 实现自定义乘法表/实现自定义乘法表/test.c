#define _CRT_SECURE_NO_WARNINGS
#include <stdio.h>
#include <stdlib.h>

void main() {
	printf("***---------------------------------------------------------------***\n");
	printf("***--------------------------乘法口诀表---------------------------***\n");
	printf("***---------------------------------------------------------------***\n");
	printf("\n");
	int i, j;
	int k;
	printf("请输入你要的乘法表规格，例如：需要9*9则输入 9。\n");
	printf("请输入：");
	scanf("%d", &k);
	for (i = 1; i <= k; i++) {
		for (j = 1; j <= i; j++) {
			printf("%d*%d=%-2d ", j, i, i*j);
			if (i == j) {
				printf("\n");
			}
		}
	}
	printf("\n");
	system("pause");
	return 0;
}